    WHEN lower(title) LIKE '%feedback%' OR lower(title) LIKE '%summarizer%' THEN 'WORKFLOW_AUTOMATION'
    WHEN lower(title) LIKE '%battlecard%' THEN 'PLAYBOOK'
    WHEN lower(title) LIKE '%onboarding%' THEN 'HANDOVER_PACK'
    WHEN lower(title) LIKE '%brief%' THEN 'ANALYTICS_BRIEF'
